#include "led.h"


void LED_Init()
{
	RCC->APB2ENR |= 1<<4;
	
	GPIO_LED->CRH &= 0xff0fffff;
	GPIO_LED->CRH |= 0x00300000;
	
	GPIO_LED->ODR &= ~(1<<13);
}


